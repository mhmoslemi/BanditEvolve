    # Identify the three most constrained circles
    constraint_severity = np.zeros(n)
    for i in range(n):
        for j in range(i + 1, n):
            dx = v[3*i] - v[3*j]
            dy = v[3*i+1] - v[3*j+1]
            dist = np.sqrt(dx*dx + dy*dy)
            if dist < v[3*i+2] + v[3*j+2] - 1e-5:
                # Higher curvature = more severe constraint
                curvature = (v[3*i+2] + v[3*j+2]) / dist
                constraint_severity[i] += curvature
                constraint_severity[j] += curvature

    # Sort indices by constraint severity (most constrained first)
    most_constrained = np.argsort(constraint_severity)[-3:]
    
    # Apply controlled radial displacement to the most constrained circles
    displacement = 0.05
    for i in most_constrained:
        # Compute direction away from neighbors
        total_dx = 0.0
        total_dy = 0.0
        for j in range(n):
            if i != j:
                dx = v[3*i] - v[3*j]
                dy = v[3*i+1] - v[3*j+1]
                dist = np.sqrt(dx*dx + dy*dy)
                if dist > 1e-5:
                    total_dx += dx / dist
                    total_dy += dy / dist
        # Normalize direction
        norm = np.sqrt(total_dx**2 + total_dy**2)
        if norm > 1e-5:
            total_dx /= norm
            total_dy /= norm
        # Displace
        v[3*i] += total_dx * displacement
        v[3*i+1] += total_dy * displacement
