import numpy as np

def run_packing():
    n = 26
    cols = int(np.ceil(np.sqrt(n)))
    # Use hexagonal grid seeding for better initial packing
    grid = np.zeros((cols, cols), dtype=float)
    for i in range(cols):
        for j in range(cols):
            grid[i, j] = (i + 0.5) / cols
            grid[i, j] += (j + 0.5) / cols * np.sqrt(3) / 2
    # Flatten grid and adjust to unit square
    xs = np.ravel(grid)
    ys = np.ravel(grid)
    # Ensure we have exactly 26 points
    if len(xs) > n:
        xs = xs[:n]
        ys = ys[:n]
    # Set initial radii based on grid spacing
    r0 = 0.5 / (cols + 1)
    v0 = np.empty(3 * n)
    v0[0::3] = xs
    v0[1::3] = ys
    v0[2::3] = r0

