        r_radii = v[2::3]
        small_circle_indices = np.argsort(r_radii)[:int(n*0.2)]
        # Perturb their positions slightly
        perturbation = 0.02 * np.random.rand(3 * n)
        v_shaken = v + perturbation
        # Ensure perturbations don't cause overlap
        for idx in small_circle_indices:
            v_shaken[3*idx] = np.clip(v_shaken[3*idx], 0.0, 1.0)
            v_shaken[3*idx+1] = np.clip(v_shaken[3*idx+1], 0.0, 1.0)
            v_shaken[3*idx+2] = np.clip(v_shaken[3*idx+2], 1e-4, 0.5)
